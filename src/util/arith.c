size_t popcnt(const uint8_t* data, size_t size) {
	size_t i = 0;
	size_t cnt = 0;

#if UNALIGN_ACCESS
	for(; i < size - size % 32; i += 32) {
		cnt += POPCNT64(*(const uint64_t*)(data + i + 0));
		cnt += POPCNT64(*(const uint64_t*)(data + i + 8));
		cnt += POPCNT64(*(const uint64_t*)(data + i + 16));
		cnt += POPCNT64(*(const uint64_t*)(data + i + 24));
	}
	if(i < size - size % 16) {
		cnt += POPCNT64(*(const uint64_t*)(data + i + 0));
		cnt += POPCNT64(*(const uint64_t*)(data + i + 8));
		i += 16;
	}
	if(i < size - size % 8) {
		cnt += POPCNT64(*(const uint64_t*)(data + i));
		i += 8;
	}
	if(i < size - size % 4) {
		cnt += POPCNT32(*(const uint32_t*)(data + i));
		i += 4;
	}
	if(i < size - size % 2) {
		cnt += POPCNT32(*(const uint16_t*)(data + i));
		i += 2;
	}
	if(i < size)
		cnt += POPCNT8(data[i]);
#else
	if(size >= 8) {
		// Align memory to an 8 byte boundary
		for(; (uintptr_t)(data + i) % 8; i++)
			cnt += POPCNT8(data[i]);
		for(; i + 7 < size; i += 8)
			cnt += POPCNT64(*(const uint64_t*)(data + i));
	}

	if(i < size) {
		uint64_t val = 0;
		size_t bytes = size - i;
		memcpy(&val, &data[i], bytes);
		cnt += POPCNT64(val);
	}
#endif

	return cnt;
}

static const uint8_t reverse_lookup[16] = {
	0x0, 0x8, 0x4, 0xc, 0x2, 0xa, 0x6, 0xe,
	0x1, 0x9, 0x5, 0xd, 0x3, 0xb, 0x7, 0xf
};

// Reversing the bits in a byte with a look up table
// So this operation can be done with 16 bytes of memory and two memory operations
uint8_t byte_reverse(uint8_t n) {
	return (reverse_lookup[n & 0b1111] << 4) | reverse_lookup[n >> 4];
}

bool power_of(uint64_t x, uint64_t n) {
	if(x < 1)
		return false;
	while(x % n == 0)
		x /= n;
	return x == 1;
}

